#include "iostream"
using namespace std;

int main() {
    //整型（整数）
    21;

    //实型（小数）
    180.5;

    //字符,''进行包围，单个字符
    'c'; //只能提供一个字符，只能是一个，不能是0或更多

    //字符串，""进行包围，任意个字符
    "";
    "c";
    "itheima";

    //通过cout将这些内容打印到控制台上
    cout << 21 << endl;
    cout << 180.5 << endl;
    cout << 'c' << endl;
    cout << "" << endl;
    cout << "c" << endl;
    cout << "itheima" << endl;

    return 0;
}

